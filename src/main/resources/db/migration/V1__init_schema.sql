-- Name: vw_historial_por_dimension; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.vw_historial_por_dimension AS
 SELECT ev.estudiante_id,
    (((e.nombre)::text || ' '::text) || (e.apellido1)::text) AS nombre_completo,
    p.numero_periodo,
    p.nombre AS periodo,
    al.anio AS anio_lectivo,
    d.nombre AS dimension,
    d.peso AS dimension_peso,
    round(avg(esc.valor_numerico), 2) AS promedio_dimension
   FROM (((((((public.evaluaciones ev
     JOIN public.periodos p ON ((p.id = ev.periodo_id)))
     JOIN public.anios_lectivos al ON ((al.id = p.anio_lectivo_id)))
     JOIN public.detalle_evaluacion de ON ((de.evaluacion_id = ev.id)))
     JOIN public.criterios_indicadores ci ON ((ci.id = de.criterio_id)))
     JOIN public.dimensiones_evaluacion d ON ((d.id = ci.dimension_id)))
     JOIN public.escalas_valoracion esc ON ((esc.id = de.escala_id)))
     JOIN public.estudiantes e ON ((e.id = ev.estudiante_id)))
  WHERE ((e.estado)::text = 'ACTIVO'::text)
  GROUP BY ev.estudiante_id, e.nombre, e.apellido1, p.numero_periodo, p.nombre, al.anio, d.id, d.nombre, d.peso
  ORDER BY ev.estudiante_id, al.anio, p.numero_periodo, d.nombre;


