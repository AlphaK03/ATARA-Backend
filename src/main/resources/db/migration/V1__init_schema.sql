     JOIN public.detalle_evaluacion de ON ((de.evaluacion_id = ev.id)))
     JOIN public.escalas_valoracion esc ON ((esc.id = de.escala_id)))
  WHERE ((p.activo = true) AND ((e.estado)::text = 'ACTIVO'::text))
  GROUP BY e.id, e.nombre, e.apellido1, n.nombre, sec.nombre, ce.nombre, p.nombre;


