CREATE VIEW public.vw_alertas_activas AS
 SELECT a.id AS alerta_id,
    a.tipo_alerta,
    a.nivel_alerta,
    a.fecha_generacion,
    a.motivo,
    e.id AS estudiante_id,
    (((e.nombre)::text || ' '::text) || (e.apellido1)::text) AS nombre_estudiante,
    ce.nombre AS centro,
    n.nombre AS nivel,
    sec.nombre AS seccion,
    m.nombre AS materia,
    c.nombre AS contenido_afectado,
    (((u.nombre)::text || ' '::text) || (u.apellidos)::text) AS generada_por,
    p.nombre AS periodo
   FROM (((((((((public.alertas a
     JOIN public.estudiantes e ON ((e.id = a.estudiante_id)))
     JOIN public.periodos p ON ((p.id = a.periodo_id)))
     JOIN public.matriculas mat ON (((mat.estudiante_id = e.id) AND (mat.anio_lectivo_id = p.anio_lectivo_id))))
     JOIN public.secciones sec ON ((sec.id = mat.seccion_id)))
     JOIN public.niveles n ON ((n.id = sec.nivel_id)))
     JOIN public.centros_educativos ce ON ((ce.id = sec.centro_id)))
     JOIN public.contenidos c ON ((c.id = a.contenido_id)))
     JOIN public.materias m ON ((m.id = c.materia_id)))
     LEFT JOIN public.usuarios u ON ((u.id = a.generada_por)))
  WHERE ((a.estado)::text = 'ACTIVA'::text)
  ORDER BY
        CASE a.nivel_alerta
            WHEN 'ALTO'::text THEN 1
            WHEN 'MEDIO'::text THEN 2
            WHEN 'BAJO'::text THEN 3
            ELSE NULL::integer
        END, a.fecha_generacion DESC;


