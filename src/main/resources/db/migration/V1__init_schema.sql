    det.eje_tematico_id,
    ej.nombre AS eje_nombre,
    ej.tipo_saber_id,
    ts.nombre AS tipo_saber_nombre,
    count(det.id) AS total_evaluaciones,
    round(avg(det.valor), 2) AS promedio,
    min(det.valor) AS valor_minimo,
    max(det.valor) AS valor_maximo
   FROM ((((public.detalle_evaluacion_saber det
     JOIN public.evaluaciones_saber es ON ((es.id = det.evaluacion_saber_id)))
     JOIN public.ejes_tematicos ej ON ((ej.id = det.eje_tematico_id)))
     JOIN public.tipos_saber ts ON ((ts.id = ej.tipo_saber_id)))
     JOIN public.materias m ON ((m.id = es.materia_id)))
  GROUP BY es.estudiante_id, es.periodo_id, es.materia_id, m.nombre, det.eje_tematico_id, ej.nombre, ej.tipo_saber_id, ts.nombre;


